import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const check = process.argv.includes('--check');

function readDirNames(dir) {
  return readdirSync(join(root, dir), { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort((a, b) => a.localeCompare(b));
}

function readFileNames(dir, suffix) {
  return new Set(
    readdirSync(join(root, dir), { withFileTypes: true })
      .filter((entry) => entry.isFile() && entry.name.endsWith(suffix))
      .map((entry) => entry.name.slice(0, -suffix.length)),
  );
}

function componentSummary(file) {
  if (!existsSync(file)) {
    return 'Missing README.';
  }

  const text = readFileSync(file, 'utf8');
  const firstParagraph = text
    .split('\n')
    .map((line) => line.trim())
    .find((line) => line.length > 0 && !line.startsWith('#'));

  return firstParagraph ?? 'No summary.';
}

const adapters = readFileNames('src/adapters/blessed', '.ts');
const publicTests = readFileNames('tests/public-api', '.test.ts');
const integrationTests = readFileNames('tests/blessed-integration', '.test.ts');
const storySource = readFileSync(join(root, 'examples/preview-prototype/stories.ts'), 'utf8');
const storyIds = new Set(
  [...storySource.matchAll(/id: '([^']+)'/g)].map((match) => match[1].split('/')[0]),
);
const rows = [];

for (const category of readDirNames('src/components')) {
  for (const name of readDirNames(`src/components/${category}`)) {
    const componentDir = `src/components/${category}/${name}`;
    const indexPath = join(root, componentDir, 'index.ts');

    if (!existsSync(indexPath)) {
      rows.push({
        adapter: 'n/a',
        category,
        docs: 'missing',
        integration: 'n/a',
        name,
        publicApi: 'n/a',
        status: 'orphan dir',
        stories: 'n/a',
        summary: 'Directory has no component entrypoint.',
      });
      continue;
    }

    const hasAdapter = adapters.has(name);

    rows.push({
      adapter: hasAdapter ? 'yes' : 'pure',
      category,
      docs: existsSync(join(root, componentDir, 'README.md')) ? 'yes' : 'missing',
      integration: hasAdapter ? (integrationTests.has(name) ? 'yes' : 'missing') : 'n/a',
      name,
      publicApi: publicTests.has(name) ? 'yes' : 'missing',
      status: hasAdapter ? 'adapter' : 'pure',
      stories: storyIds.has(name) ? 'yes' : 'missing',
      summary: componentSummary(join(root, componentDir, 'README.md')),
    });
  }
}

const content = `# Component Index

Generated by \`npm run docs:index\`.

| Category | Component | Status | Adapter | Public test | Integration test | Story | Docs | Summary |
| --- | --- | --- | --- | --- | --- | --- | --- | --- |
${rows
  .map(
    (row) =>
      `| ${row.category} | \`${row.name}\` | ${row.status} | ${row.adapter} | ${row.publicApi} | ${row.integration} | ${row.stories} | ${row.docs} | ${row.summary.replaceAll('|', '\\|')} |`,
  )
  .join('\n')}
`;
const outputPath = join(root, 'docs/component-index.md');

if (check) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : '';

  if (current !== content) {
    throw new Error('docs/component-index.md is stale. Run `npm run docs:index`.');
  }
} else {
  writeFileSync(outputPath, content);
}

console.log(`Component index ${check ? 'checked' : 'generated'}: ${rows.length} entries.`);
